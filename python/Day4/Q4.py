# 4. Write a Python program to remove item(s) from a given set. 
# Sample Output:
# Original set:
# {0, 1, 3, 4, 5}
# After removing the first element from the said set:
# {1, 3, 4, 5}

var = {0, 1, 3, 4, 5}
var.remove(0)
print(f"Original set:\n{{0, 1, 3, 4, 5}}\nAfter removing the first element from the said set:\n{var}")
